package emulator

// PPURAM PPUからアクセスできるメモリマップ。CPUから直接アクセスはできない。
type PPURAM struct {
	pattern0    [0x1000]byte // 0x0000-0x0FFF
	pattern1    [0x1000]byte // 0x1000-0x1FFF
	name0       [0x03c0]byte // 0x2000-0x23BF
	attr0       [0x0040]byte // 0x23C0-0x23FF
	name1       [0x03c0]byte // 0x2400-0x27BF
	attr1       [0x0040]byte // 0x27C0-0x27FF
	name2       [0x03c0]byte // 0x2800-0x2BBF
	attr2       [0x0040]byte // 0x2BC0-0x2BFF
	name3       [0x03c0]byte // 0x2C00-0x2FBF
	attr3       [0x0040]byte // 0x2FC0-0x2FFF
	mirror0     [0xf00]byte  // 0x3000-0x3EFF
	bgPalettes  [0x0010]byte // 0x3F00-0x3F0F
	sprPalettes [0x0010]byte // 0x3F10-0x3F1F
	mirror1     [0x00e0]byte // 0x3F20-0x3FFF
}
